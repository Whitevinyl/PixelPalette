class RGBA {

    public R: number;
    public G: number;
    public B: number;
    public A: number;

    constructor(r: number, g: number, b: number, a: number) {
        this.R = r;
        this.G = g;
        this.B = b;
        this.A = a;
    }

}

export = RGBA;